"""#數字運算"""
x = 3+6
print(x)
x = 3-6
print(x)
x = 3*6
print(x)
x = 3**3  # **幾次方
print(x)
x = 7/6
print(x)
x = 7//6  # 取整數
print('取整數', x)
# x=7%3 #取餘數
print(x)
# 特殊範例 x=2+3
print('特殊運算式')
x = 2+3
x += 1  # 變形 #x=x+1(原型) #將變數中的數字+n (n=1)
print(x)
# x=2+3
x -= 1  # 其他兄弟 #x=x-1(原型)
print(x)
x = 2+3
x *= 1  # 其他兄弟 #x=x*1(原型)
print(x)
x = 2+3
x /= 1  # 其他兄弟 #x=x/1(原型)
print(x)
x = 2+3
n = 45
x += n  # 變形 #x=x+n(原型)
print(x)
